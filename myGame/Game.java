import java.util.ArrayList;
import java.util.HashMap;

/**
 *  This class is the main class of the "World of Zuul" application. 
 *  "World of Zuul" is a very simple, text based adventure game.  Users 
 *  can walk around some scenery. That's all. It should really be extended 
 *  to make it more interesting!
 *
 *  To play this game, create an instance of this class and call the "play"
 *  method.
 * 
 *  This main class creates and initialises all the others: it creates all
 *  rooms, creates the parser and starts the game.  It also evaluates and
 *  executes the commands that the parser returns.
 * 
 * @author  Michael Kölling and David J. Barnes
 * @version 2016.02.29
 */

public class Game 
{
    private Parser parser;
    private Room currentRoom;

    /**
     * Create the game and initialise its internal map.
     */
    public Game() 
    {
        createRooms();
        parser = new Parser();
    }

    /**
     * Create all the rooms and link their exits together.
     */
    private void createRooms()
    {
        Room rua, entrada, restaurante, recepcao, sala1, sala2, sala3, sala4, armazem, copa;

        // create the rooms
        rua = new Room("na rua em frente ao cinema em que aconteceu o assassinato.");
        entrada = new Room("no hall de entrada do cinema.");
        restaurante = new Room("no restaurante dentro do cinema, atualmente não há ninguem trabalhando.");
        recepcao = new Room("na recepção do cinema, existem alguns sinais de sangue perto a uma das salas.");
        sala1 = new Room("uma das salas do cinema. Não há nada aqui.");
        sala2 = new Room("uma das salas do cinema. O corpo da vítima se encontra em um assento no fundo da sala.");
        armazem = new Room("na sala onde os funcionários guardam estoque de alimentos");
        copa = new Room("na Área exclusiva para funcionarios, existe uma estante cheia de produtos de limpeza e outros itens");

        // initialise room exits
        rua.setExit("south", entrada);

        entrada.setExit("north", rua);
        entrada.setExit("west", restaurante);
        entrada.setExit("south", recepcao);

        restaurante.setExit("west", entrada);

        recepcao.setExit("north", entrada);
        recepcao.setExit("east", sala2);
        recepcao.setExit("west", sala1);
        recepcao.setExit("south", copa);


        copa.setExit("north", recepcao);
        copa.setExit("west", armazem);

        armazem.setExit("east", copa);


        //initialise items

        currentRoom = rua;  // start game outside
    }


    /**
     * Adds an item to the list of items in a room.
     * @param itemName The item you want to add.
     */


    /**
     *  Main play routine.  Loops until end of play.
     */
    public void play() 
    {            
        printWelcome();

        // Enter the main command loop.  Here we repeatedly read commands and
        // execute them until the game is over.
                
        boolean finished = false;
        while (! finished) {
            Command command = parser.getCommand();
            finished = processCommand(command);
        }
        System.out.println("Thank you for playing.  Good bye.");
    }

    /**
     * Print out the opening message for the player.
     */
    private void printWelcome()
    {
        System.out.println();
        System.out.println("Welcome to the World of Zuul!");
        System.out.println("World of Zuul is a new, incredibly boring adventure game.");
        System.out.println("Type 'help' if you need help.");
        System.out.println();
        System.out.println(currentRoom.getLongDescription());
    }

    /**
     * Given a command, process (that is: execute) the command.
     * @param command The command to be processed.
     * @return true If the command ends the game, false otherwise.
     */
    private boolean processCommand(Command command) 
    {
        boolean wantToQuit = false;

        if(command.isUnknown()) {
            System.out.println("I don't know what you mean...");
            return false;
        }

        String commandWord = command.getCommandWord();
        if (commandWord.equals("help")) {
            printHelp();
        }
        else if (commandWord.equals("go")) {
            goRoom(command);
        }
        else if (commandWord.equals("quit")) {
            wantToQuit = quit(command);
        }
        // else command not recognised.
        return wantToQuit;
    }

    // implementations of user commands:

    /**
     * Print out some help information.
     * Here we print some stupid, cryptic message and a list of the 
     * command words.
     */
    private void printHelp() 
    {
        System.out.println("You are lost. You are alone. You wander");
        System.out.println("around at the university.");
        System.out.println();
        System.out.println("Your command words are:");
        parser.showCommands();
    }

    /** 
     * Try to in to one direction. If there is an exit, enter the new
     * room, otherwise print an error message.
     */
    private void goRoom(Command command) 
    {
        if(!command.hasSecondWord()) {
            // if there is no second word, we don't know where to go...
            System.out.println("Go where?");
            return;
        }

        String direction = command.getSecondWord();

        // Try to leave current room.
        Room nextRoom = currentRoom.getExit(direction);

        if (nextRoom == null) {
            System.out.println("There is no door!");
        }
        else {
            currentRoom = nextRoom;
            System.out.println(currentRoom.getLongDescription());
        }
    }

    /** 
     * "Quit" was entered. Check the rest of the command to see
     * whether we really quit the game.
     * @return true, if this command quits the game, false otherwise.
     */
    private boolean quit(Command command) 
    {
        if(command.hasSecondWord()) {
            System.out.println("Quit what?");
            return false;
        }
        else {
            return true;  // signal that we want to quit
        }
    }
}
